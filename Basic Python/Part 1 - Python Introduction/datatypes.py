'''
PYTHON: DATA TYPES
Text type : str
Number type : int, float
Boolean type : bool

String : is a sequence of characters, surrounded by either single or double quotation marks.
Int : is a whole number, positive or negative, without decimals, of unlimited length.
Float : is a number, positive or negative, containing one or more decimals.
Boolean : represent one of two values of True or False.

Example                     Data Type
x = "Hello, World!"         str
x = 10                      int   
x = 10.5                    float
x = True                    boolean
'''

# You can get the data type of any object by using the type()

# integer
x = 5
print(type(x))

# string
y = "Hello, World!"
print(type(y))

# float
z = 10.5
print(type(z))

# boolean
a = True
print(type(a))